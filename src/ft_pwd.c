#include "minishell.h"

void	ft_pwd(char *args[]) 
{
	char	buf[4096];

	(void)args;
	if (getcwd(buf, 4096) == NULL)
		exit(1); // 비정상 종료 => error
	printf("%s\n", buf);
}
